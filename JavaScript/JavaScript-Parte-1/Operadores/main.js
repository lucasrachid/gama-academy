// Operadores Aritméticos
// Adição +
let sumTwoNumbers = 1 + 3;
console.log(sumTwoNumbers);

// Subtração -
let subTwoNumbers = 5 - 2;
console.log(subTwoNumbers);

// Multiplicação *
let mulTwoNumbers = 5 * 2;
console.log(mulTwoNumbers);

// Divisão /
let divTwoNumbers = 5 / 5;
console.log(divTwoNumbers);

// Módulo ou resto de uma divisão %
let divTwoNumbers2 = 5 / 4;
console.log(parseInt(divTwoNumbers2));
//Quando eu quero declarar, que a divisão deve ser realizada somente com numeros inteiros, devo colocar parseInt() ou pode realizar a conta da seguinte forma:
console.log(8 % 3);

// incremento ++
let num = 1;
console.log(++num);

// decremento --
console.log(--num);

